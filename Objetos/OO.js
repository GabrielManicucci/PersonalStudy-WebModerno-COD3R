/* Possui 4 conceitos base muito importantes
 
  - Abastração: é sobre como o programador precisa entender o mundo a sua volta a fim de transformar-lo em softwares coesos e precisos

  - Encapsulamento: é o fato de esconder os detalhes de implementação de código de um objeto, a fim de dificultar a depdência entre códigos e favorecer a evolução do sistema de maneira mais daudável e menos complexa

  - Herança (prototype)

  - Poliomorfismo: multiplas formas de um objeto pai possui objetos filhos que fazem parte da mesma relação */